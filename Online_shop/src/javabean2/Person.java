package javabean2;

public class Person {
   private String person_id;
   private String person_name;
   private String person_img;
   private int person_professor;
   private String person_call;
   private String person_pwd;
public String getPerson_id() {
	return person_id;
}
public void setPerson_id(String person_id) {
	this.person_id = person_id;
}
public String getPerson_name() {
	return person_name;
}
public void setPerson_name(String person_name) {
	this.person_name = person_name;
}
public String getPerson_img() {
	return person_img;
}
public void setPerson_img(String person_img) {
	this.person_img = person_img;
}
public int getPerson_professor() {
	return person_professor;
}
public void setPerson_professor(int person_professor) {
	this.person_professor = person_professor;
}
public String getPerson_call() {
	return person_call;
}
public void setPerson_call(String person_call) {
	this.person_call = person_call;
}
public String getPerson_pwd() {
	return person_pwd;
}
public void setPerson_pwd(String person_pwd) {
	this.person_pwd = person_pwd;
}
}
