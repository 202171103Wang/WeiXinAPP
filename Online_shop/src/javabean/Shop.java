package javabean;

public class Shop {
	private String Shop_id;
	private String Shop_name;
	private String Shop_img;
	private int Shop_state;
	private String Shop_place;
	public String getShop_id() {
		return Shop_id;
	}
	public void setShop_id(String shop_id) {
		Shop_id = shop_id;
	}
	public String getShop_name() {
		return Shop_name;
	}
	public void setShop_name(String shop_name) {
		Shop_name = shop_name;
	}
	public String getShop_img() {
		return Shop_img;
	}
	public void setShop_img(String shop_img) {
		Shop_img = shop_img;
	}
	public int getShop_state() {
		return Shop_state;
	}
	public void setShop_state(int shop_state) {
		Shop_state = shop_state;
	}
	public String getShop_place() {
		return Shop_place;
	}
	public void setShop_place(String shop_place) {
		Shop_place = shop_place;
	}
	

}
